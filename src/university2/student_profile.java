    private void information(int username) {
     
        try {
            con=connect();
            p=con.prepareStatement("select * from student where id='"+username+"'");
            res=p.executeQuery();
            res.next();
            number.setText(res.getString(1));
            name.setText(res.getString(2));
            phone.setText(res.getString(4));
            gender.setText(res.getString(5));
            avg.setText(res.getString(6));
            required.setText(res.getString(7)+" $");
            paid.setText(res.getString(8)+" $");
            fees.setText(res.getString(9)+" $");

;

        } catch (SQLException ex) {
            Logger.getLogger(student_profile.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
             
                 try {
                     con.close();
                      p.close();
                     res.close();
                 } catch (SQLException ex) {
                     Logger.getLogger(student.class.getName()).log(Level.SEVERE, null, ex);
                 }
                
             }
     
    }
