    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
           if(!conpass.getText().equals("") && !newpass.getText().equals("") && !oldpass.getText().equals("") )
           {
             
               try {
                   con=connect();
                   p=con.prepareStatement("select password from student where id='"+user.getText()+"'");
                   rst=p.executeQuery();
                   rst.next();
                   
                   if(oldpass.getText().equals(rst.getString("password"))){
                         p=con.prepareStatement("update student set password='"+newpass.getText()+"' where id ='"+user.getText()+"'");
                         p.execute();
                      JOptionPane.showMessageDialog(null, " password changed");
                      newpass.setText("");
                      oldpass.setText("");
                      conpass.setText("");
                      user.setText(""); 

                   }else{
                 JOptionPane.showMessageDialog(null, " error in oldpass");

                   }
               } catch (SQLException ex) {

                }finally{
                   try {
                       p.close();
                       con.close();
                       rst.close();
                   } catch (SQLException ex) {
                       Logger.getLogger(changepassword.class.getName()).log(Level.SEVERE, null, ex);
                   }
                       
                 
               }
             
           }else{
             JOptionPane.showMessageDialog(null, " Enter valid information");

           }  
    }//GEN-LAST:event_jButton1ActionPerformed

