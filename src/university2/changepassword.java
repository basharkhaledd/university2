    private void newpassKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_newpassKeyReleased
      String oldp=String.valueOf(oldpass.getPassword());
      String newp=String.valueOf(newpass.getPassword());
      
      if(!oldpass.getText().equals("") && !newpass.getText().equals("") ){
         if(oldp.equals(newp) ) {
          ch.setText("oldpass same newpass");
          ch.setForeground(Color.red);
          ch.setVisible(true);
          conpass.setRequestFocusEnabled(false);
      }
         else{
            ch.setText("");
          ch.setForeground(Color.black);
          conpass.setRequestFocusEnabled(true);
          ch.setVisible(false);
          
         }
      }
      String conp=String.valueOf(conpass.getPassword());
      String newp1=String.valueOf(newpass.getPassword());
      
      if(!conpass.getText().equals("") && !newpass.getText().equals("") ){
         if(conp.equals(newp1) ) {
          ch2.setText("confirmpass same newpass ✓");
          ch2.setForeground(Color.green);
          ch2.setVisible(true);
      }
         else{
            ch2.setText("confirmpass not same newpass");
          ch2.setForeground(Color.red);
          ch2.setVisible(true);
          
         }
         }
    }//GEN-LAST:event_newpassKeyReleased

